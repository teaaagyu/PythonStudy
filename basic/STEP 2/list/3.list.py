car_brand = ["bmw", "benz", "audi", "kia", "hyundai"]

print(car_brand[0])  # "bmw" 출력
print(car_brand[1][2]) # "n" 출력
print(car_brand[-1])  # "hyundai" 출력
print(car_brand[1:3])  # ["benz", "audi"] 출력
print(car_brand[0:6:2])  # ["bmw", "audi", "hyundai"] 출력
print(car_brand[1][1:3])  # "en" 출력

print(len(car_brand))